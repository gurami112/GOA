// რიცხვებს შორის ყველა რიცხვის გაგება და მასივში შეტანა, საბოლოოდ დააბრუნეთ მასივი

function range(start,end){
    let arr = [];
    for(let i = start; start < end;i++){
        arr.push(i)
    }
    return arr;
}